//Creo dos variables con numeros
let numero1 = 5;
let numero2 = 3;

//sumo los dos numeros y lo guardo en la variable suma
let suma = numero1 + numero2;

document.write("Sumar los numeros " + numero1 + " y " + numero2 + " es igual a " + suma);

//resto los dos numeros y lo guardo en la variable resta
let resta = numero1 - numero2;

document.write(" Restar los numeros " + numero1 + " y " + numero2 + " es igual a " + resta);

//divido los dos numeros y lo guardo en la variable division
let division = numero1 / numero2;

document.write(" Dividir los numeros " + numero1 + " y " + numero2 + " es igual a " + division);

//multiplico los dos numeros y lo guardo en la variable multiplicacion
let multiplicacion = numero1 * numero2;

document.write(" Multiplico los numeros " + numero1 + " y " + numero2 + " es igual a " + multiplicacion);

//Combino varias operaciones. Calculo la media de los dos numeros y lo guardo en la variable media

let media = (numero1 + numero2) / 2;

document.write(" La media de los numeros " + numero1 + " y " + numero2 + " es igual a " + media);